# 배열 탐색
# 1. 선형 탐색: 무작위로 늘어 놓은 데이터 집합에서 탐색을 수행
# 2. 이진 탐색: 일정한 규칙으로 늘어놓은 데이터 집합에서 아주 빠른 탐색을 수행
# 3. 해시법: 추가, 삭제가 자주일어나는 데이터 집합에서 아주 빠른 탐색을 수행
#       - 체인법: 같은 해시값 데이터를 연결리스트로 연결하는 방법
#       - 오픈 주소법: 데이터를 위한 해시값이 충돌할 때 재해시하는 방법

# 선형 탐색 (Linear search), 순차 검색(sequential search)
